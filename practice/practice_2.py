# BMI of the user

# Height of the User
height = input("Enter your height in Metres: ")

# Weight of the user
Weight = input("Enter your weight in KiloGrams: ")

# Calculating BMI (formula(BMI) = Weight/Height**2)
BMI = int(Weight)/float(height)**2

#Printing BMI
print(BMI,"\n")

# BMI as integer

print(int(BMI))
